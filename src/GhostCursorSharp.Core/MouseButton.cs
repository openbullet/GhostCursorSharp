namespace GhostCursorSharp;

/// <summary>
/// Identifies the mouse button used for click actions.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The primary button, usually the left mouse button.
    /// </summary>
    Left = 0,

    /// <summary>
    /// The auxiliary button, usually the middle mouse button.
    /// </summary>
    Middle = 1,

    /// <summary>
    /// The secondary button, usually the right mouse button.
    /// </summary>
    Right = 2
}
